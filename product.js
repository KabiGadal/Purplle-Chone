function showProduct(d) {
    let  product = d
    let data_div = document.getElementById('prod');
    data_div.innerHTML = null;

    product.forEach(function (p,i) {
        let div = document.createElement('div');

        let name = document.createElement('p');
        name.innerHTML = p.desc;

        let price = document.createElement('h3');
        price.innerHTML = p.price;

        let image = document.createElement('img');
        image.src = p.image;

        let rating = document.createElement("button");
        rating.innerText = p.rating;
        rating.setAttribute('class','rating')
      

        div.append(image, name, price,rating);
        div.addEventListener('click', function () {
            let index = i;
            localStorage.setItem('index', JSON.stringify(index));
            window.location.href = 'productDesc.html';
        });
        div.style.cursor = 'pointer';

        data_div.append(div);
    })
}
